function mostrarSubirPagina() {
	var flechasubir = document.getElementById("flechasubir");
	flechasubir.style.display = "block";
}

function noMostrarSubirPagina() {
	var flechasubir = document.getElementById("flechasubir");
	flechasubir.style.display = "none";
}
